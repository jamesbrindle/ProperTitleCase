        /// <returns>An array of strings converted to title case.</returns>
        public static string[] ToProperTitleCase(
            this string[] input,
            List<string> additionalAbbreviations = null,
            bool lookupCommonAbbreviations = true,
            bool keepTypicalAllLowers = true,
            bool formatMeasurements = true,
            bool removeStartEndQuotesOnClean = true,
            bool removeDoubleSymbols = true)
        {
            // Converts an array of strings to title case using the specified options.
            return ToProperTitleCase(
                input.ToList(),
                additionalAbbreviations,
                lookupCommonAbbreviations,
                keepTypicalAllLowers,
                formatMeasurements,
                removeStartEndQuotesOnClean,
                removeDoubleSymbols).ToArray();
        }

        /// <summary>
        /// Converts an enumerable collection of strings to title case.
        /// </summary>
        /// <param name="input">The collection of strings to convert.</param>
        /// <param name="additionalAbbreviations">A list of additional abbreviations to consider during conversion.</param>
        /// <param name="lookupCommonAbbreviations">Whether to look up common abbreviations.</param>
        /// <param name="keepTypicalAllLowers">Whether to keep typically lowercase words in lower case.</param>
        /// <param name="formatMeasurements">Whether to format measurement strings.</param>
        /// <param name="removeStartEndQuotesOnClean">Remove preceeding and leeding apostrophe or double quote (if end quote is same at start quote and if end quote is not a measurement, i.e.19")</param>
        /// <param name="removeDoubleSymbols">Remove doulbe symbols ( "", '', --) on clean</param>
        /// <returns>An enumerable collection of strings converted to title case.</returns>
        public static IEnumerable<string> ToProperTitleCase(
            IEnumerable<string> input,
            List<string> additionalAbbreviations = null,
            bool lookupCommonAbbreviations = true,
            bool keepTypicalAllLowers = true,
            bool formatMeasurements = true,
            bool removeStartEndQuotesOnClean = true,
            bool removeDoubleSymbols = true)
        {
            // Converts a collection of strings to title case using the specified options.
            IEnumerable<string> output =
                ToProperTitleCase(
                    input.ToList(),
                    additionalAbbreviations,
                    lookupCommonAbbreviations,
                    keepTypicalAllLowers,
                    formatMeasurements,
                    removeStartEndQuotesOnClean,
                    removeDoubleSymbols);

            return output;
        }

        /// <summary>
        /// Converts a list of titles to title case.
        /// </summary>
        /// <param name="input">The list of titles to convert.</param>
        /// <param name="additionalAbbreviations">A list of additional abbreviations to consider during conversion.</param>
        /// <param name="lookupCommonAbbreviations">Whether to look up common abbreviations.</param>
        /// <param name="keepTypicalAllLowers">Whether to keep typically lowercase words in lower case.</param>
        /// <param name="formatMeasurements">Whether to format measurement strings.</param>
        /// <param name="removeStartEndQuotesOnClean">Remove preceeding and leeding apostrophe or double quote (if end quote is same at start quote and if end quote is not a measurement, i.e.19")</param>
        /// <param name="removeDoubleSymbols">Remove doulbe symbols ( "", '', --) on clean</param>
        /// <returns>A list of titles converted to title case.</returns>
        public static List<string> ToProperTitleCase(
            this List<string> input,
            List<string> additionalAbbreviations = null,
            bool lookupCommonAbbreviations = true,
            bool keepTypicalAllLowers = true,
            bool formatMeasurements = true,
            bool removeStartEndQuotesOnClean = true,
            bool removeDoubleSymbols = true)
        {
            // Initialize variables and process each title in the list.
            var textInfo = new CultureInfo("en-GB", false).TextInfo;
            var processedTitles = new List<string>();

            // Custom processing based on the provided options.
            RemoveFromLowercase(additionalAbbreviations);
            RemoveFromSmallWords(additionalAbbreviations);
            RemoveFromAbbreviatedDayOrMonth(additionalAbbreviations);

            // Iterate through each title and process each word.
            foreach (string title in input)
            {
                if (!string.IsNullOrWhiteSpace(title))
                {
                    var words = title.ToLower().Split(new[] { ' ' }, StringSplitOptions.RemoveEmptyEntries);
                    var processedWords = words.Select(word => ProcessWord(word, additionalAbbreviations, lookupCommonAbbreviations, keepTypicalAllLowers, textInfo)).ToArray();

                    // Apply measurement formatting if specified.
                    if (formatMeasurements)
                    {
                        processedTitles.Add(
                            FormatMeasurementString(
                                CleanedOutput(
                                    string.Join(" ", processedWords),
                                    removeStartEndQuotesOnClean,
                                    removeDoubleSymbols)));
                    }
                    else
                    {
                        processedTitles.Add(
                            CleanedOutput(
                                string.Join(" ", processedWords),
                                removeStartEndQuotesOnClean,
                                removeDoubleSymbols));
                    }
                }
            }

            return processedTitles;
        }

        // Processes a single word for title casing considering various conditions.
        private static string ProcessWord(
            string word,
            List<string> additionalAbbreviations,
            bool lookupCommonAbbreviations,
            bool keepTypicalAllLowers,
            TextInfo textInfo)
        {
            // Converts emails to lowercase.
            if (IsEmail(word)) return word.ToLower();

            // Converts URL web addresses to lowercase.
            if (IsUrl(word)) return word.ToLower();

            // Converts ordinal numbers to lowercase.
            if (IsOrdinalNumber(word)) return word.ToLower();

            // Keeps certain words in lowercase if specified.
            if (keepTypicalAllLowers && Lookups.LowercaseHashSet.Contains(word.ToLower())) return word.ToLower();

            // Converts day or month abbreviations to title case.
            if (IsDayOrMonthAbbreviation(word)) return textInfo.ToTitleCase(word.ToLower());

            // Converts words containing numbers or special characters to uppercase.
            if (WordContainsNumbersOrSpecialCharacters(word)) return word.ToUpper();

            // Handles abbreviations and formats them accordingly.
            if (IsAbbreviation(word, additionalAbbreviations, lookupCommonAbbreviations, out string formattedAbbreviation)) return formattedAbbreviation;

            // Converts short words to uppercase unless they are in the small words dictionary.
            if (RemoveStartOrEndsWithSpecialCharacter(word, out char? _, out char? _).Length < 5 && !IsSmallWordInSmallWordDictionary(word)) return word.ToUpper();

            // Special handling for words ending with 's or 's".
            if (RemoveStartOrEndsWithSpecialCharacter(word, out char? _, out char? _).Length < 7 && word.ToLower().EndsWith("'s") && !IsSmallWordInSmallWordDictionary(word.ToLower().Replace("'s", ""))) return word.ToUpper().Replace("'S", "") + "'s";
            if (RemoveStartOrEndsWithSpecialCharacter(word, out char? _, out char? _).Length < 7 && word.ToLower().EndsWith("'s\"") && !IsSmallWordInSmallWordDictionary(word.ToLower().Replace("'s\"", ""))) return word.ToUpper().Replace("'S\"", "") + "'s\"";

            // Default case: converts the word to title case.
            return textInfo.ToTitleCase(word.ToLower());
        }

        // Cleans the output string from extra spaces and misplaced quotation marks.
        private static string CleanedOutput(string text, bool removeStartEndQuotesOnClean, bool removeDoubleSymbols)
        {
            if (string.IsNullOrEmpty(text))
                return string.Empty;

            // Removes extra spaces and trims the word.
            string cleaned = text.Replace("  ", " ").Trim();

            if (removeDoubleSymbols)
            {
                cleaned = cleaned.Replace("\"\"", "\"");
                cleaned = cleaned.Replace("''", "'");
                cleaned = cleaned.Replace("--", "-");
                cleaned = cleaned.Replace("__", "_");
                cleaned = cleaned.Replace("––", "–");
            }

            try
            {
                // Handles edge cases with trailing quotation marks.
                if (cleaned.EndsWith(" \""))
                    cleaned = cleaned.Substring(0, cleaned.Length - 2) + "\"";

                // Handles edge cases with leading quotation marks.
                if (cleaned.StartsWith("\" "))
                    cleaned = cleaned.Substring(2);

                char? start = null;
                char? end = null;

                if (removeStartEndQuotesOnClean)
                {
                    char s1 = cleaned[0];
                    char e1 = cleaned[cleaned.Length - 1];

                    if (s1 == '\"' || s1 == '\'')
                    {
                        start = s1;
                        cleaned = cleaned.Substring(1);
                    }

                    if (e1 == '\"' || e1 == '\'')
                    {
                        end = e1;
                        if (e1 == s1)
                        {
                            if (!string.IsNullOrEmpty(cleaned))
                            {
                                string[] words = cleaned.Split(new char[] { ' ' }, StringSplitOptions.RemoveEmptyEntries);
                                string lastWord = (words.Length > 0 ? words[words.Length - 1] : "").Trim();

                                // make sure it's not a measurement
                                if (!Regex.IsMatch(lastWord, "\\d['\"]$"))
                                    cleaned = cleaned.Substring(0, cleaned.Length - 1);

                            }
                        }
                    }
                }
            }

            catch { }

            return cleaned.Trim();
        }

        // Formats a string that contains measurements (e.g., "5kg") to a standardized format.
        public static string FormatMeasurementString(string input)
        {
            // Uses regular expressions to identify and format measurement strings within the input.
            string result = Regex.Replace(input, Lookups.RegularExpression.MeasurmentRecognition, match =>
            {
                // Extracts the numeric value and unit from the match.
                string value = match.Groups[1].Value;
                string unit = match.Groups[3].Value;

                // Finds the correct unit from the lookup table.
                string correctUnit = Lookups.Measurements.Find(u => u.Equals(unit, StringComparison.OrdinalIgnoreCase));

                // Formats the measurement string or returns the original match if no unit is found.
                return correctUnit != null ? $"{value}{correctUnit}" : match.Value;
            });

            return result;
        }

        // Checks if the provided word is an email address.
        private static bool IsEmail(string word)
        {
            return Lookups.RegularExpression.Email.IsMatch(word);
        }

        // Checks if the provided word is a URL web address
        private static bool IsUrl(string word)
        {
            return Lookups.RegularExpression.Url.IsMatch(word);
        }

        // Checks if the provided word is an ordinal number (e.g., 1st, 2nd).
        private static bool IsOrdinalNumber(string word)
        {
            return Lookups.RegularExpression.OrdinalNumber.IsMatch(word);
        }

        // Checks if the provided word is typically kept in lowercase.
        private static bool IsSmallWordInSmallWordDictionary(string word)
        {
            return Lookups.SmallWordsHash.Contains(RemoveStartOrEndsWithSpecialCharacter(word, out char? _, out char? _).ToLower().ToLower());
        }

        // Checks if the provided word is an abbreviation for a day or month.
        private static bool IsDayOrMonthAbbreviation(string word)
        {
            return Lookups.AbbreviatedDayOrMonthHashSet.Contains(word.ToLower());
        }

        // Checks if the given word contains any numbers or special characters based on defined criteria.
        private static bool WordContainsNumbersOrSpecialCharacters(string word)
        {
            // Check if the word contains any digit. If so, return true immediately.
            if (word.Any(char.IsDigit)) return true;

            // Check for the presence of special characters in the word and specific conditions for their count.
            // If any of these conditions are met, the function returns true, indicating the presence of numbers or special characters.

            if (word.Count(c => c == '/') > 0) return true; // Check for at least one forward slash '/'
            if (word.Count(c => c == '\\') > 0) return true; // Check for at least one backslash '\'
            if (word.Count(c => c == '.') > 1) return true;  // More than one period '.'
            if (word.Count(c => c == '"') > 2) return true;  // More than two quotation marks '"'
            if (word.Count(c => c == '£') > 0) return true;  // At least one pound symbol '£'
            if (word.Count(c => c == '$') > 0) return true;  // At least one dollar sign '$'
            if (word.Count(c => c == '%') > 0) return true;  // At least one percent sign '%'
            if (word.Count(c => c == '^') > 0) return true;  // At least one caret '^'
            if (word.Count(c => c == '&') > 0) return true;  // At least one ampersand '&'
            if (word.Count(c => c == '*') > 1) return true;  // More than one asterisk '*'
            if (word.Count(c => c == '(') > 2) return true;  // More than two open parenthesis '('
            if (word.Count(c => c == ')') > 2) return true;  // More than two close parenthesis ')'
            if (word.Count(c => c == '-') > 2) return true;  // More than two hyphens '-'
            if (word.Count(c => c == '=') > 0) return true;  // At least one equal sign '='
            if (word.Count(c => c == '_') > 0) return true;  // At least one underscore '_'
            if (word.Count(c => c == '+') > 0) return true;  // At least one plus sign '+'
            if (word.Count(c => c == '`') > 0) return true;  // At least one backtick '`'
            if (word.Count(c => c == '¬') > 0) return true;  // At least one negation '¬'
            if (word.Count(c => c == '|') > 0) return true;  // At least one pipe '|'
            if (word.Count(c => c == '[') > 0) return true;  // At least one open square bracket '['
            if (word.Count(c => c == ']') > 0) return true;  // At least one close square bracket ']'
            if (word.Count(c => c == '{') > 0) return true;  // At least one open curly brace '{'
            if (word.Count(c => c == '}') > 0) return true;  // At least one close curly brace '}'
            if (word.Count(c => c == ';') > 1) return true;  // More than one semicolon ';'
            if (word.Count(c => c == '\'') > 1) return true; // More than one single quote '\''
            if (word.Count(c => c == '#') > 0) return true;  // At least one hash '#'
            if (word.Count(c => c == ':') > 1) return true;  // More than one colon ':'
            if (word.Count(c => c == '@') > 1) return true;  // More than one at sign '@'
            if (word.Count(c => c == '~') > 0) return true;  // At least one tilde '~'
            if (word.Count(c => c == ',') > 1) return true;  // More than one comma ','
            if (word.Count(c => c == '/') > 1) return true;  // More than one forward slash '/' (again, may be redundant)
            if (word.Count(c => c == '<') > 0) return true;  // At least one less than '<'
            if (word.Count(c => c == '>') > 0) return true;  // At least one greater than '>'
            if (word.Count(c => c == '?') > 1) return true;  // More than one question mark '?'
            if (word.Count(c => c == '—') > 1) return true;  // More than one em dash '—'

            return false;
        }

        // Determines if a word is an abbreviation and formats it accordingly.
        private static bool IsAbbreviation(
            string word, // The word to check for abbreviation.
            List<string> additionalAbbreviations, // List of additional abbreviations provided by the user.
            bool lookupCommonAbbreviations, // Flag indicating whether to look up common abbreviations.
            out string formattedAbbreviation) // The formatted abbreviation if the word is an abbreviation.
        {
            formattedAbbreviation = string.Empty;
            bool endsWithApostropheS = false;
            bool isAbbreviation = false;

            // Check if the word ends with "'s" (case-insensitive) and update the word and flag accordingly.
            if (word.EndsWith("'s", StringComparison.OrdinalIgnoreCase))
            {
                word = word.ToLower().Replace("'s", "");
                endsWithApostropheS = true;
            }

            string wordWithRemovedSpecialCharacters = RemoveStartOrEndsWithSpecialCharacter(word, out char? start, out char? end).ToLower();

            // Check common abbreviations if the flag is set.
            if (lookupCommonAbbreviations && Lookups.CommonAbbreviationsHashSet.Contains(wordWithRemovedSpecialCharacters))
            {
                isAbbreviation = true;

                // Construct the formatted abbreviation using the start character, abbreviation, end character, and "'s" if applicable.
                formattedAbbreviation =
                    (start == null ? "" : start.ToString()) +
                    Lookups.CommonAbbreviations.Where(s => s.Equals(wordWithRemovedSpecialCharacters, StringComparison.OrdinalIgnoreCase))
                                           .FirstOrDefault() +
                    (end == null ? "" : end.ToString()) +
                    (endsWithApostropheS ? "'s" : "");
            }

            // Check additional abbreviations provided by the user.
            if (additionalAbbreviations != null && additionalAbbreviations.Exists(s => s.Equals(wordWithRemovedSpecialCharacters, StringComparison.OrdinalIgnoreCase)))
            {
                isAbbreviation = true;

                // Construct the formatted abbreviation using the start character, abbreviation from the additional list, end character, and "'s" if applicable.
                formattedAbbreviation =
                    (start == null ? "" : start.ToString()) +
                    additionalAbbreviations.Where(s => s.Equals(wordWithRemovedSpecialCharacters, StringComparison.OrdinalIgnoreCase))
                                           .FirstOrDefault() +
                    (end == null ? "" : end.ToString()) +
                    (endsWithApostropheS ? "'s" : "");
            }

            // Return true if the word is an abbreviation, otherwise false.
            return isAbbreviation;
        }

        // Checks if the word starts with any of the special characters defined in Lookups.SpecialCharacters.
        private static bool WordStartsWithSpecialCharacter(string word)
        {
            for (int i = 0; i < Lookups.SpecialCharacters.Length; i++)
            {
                if (word.StartsWith(Lookups.SpecialCharacters[i].ToString().ToLower()))
                    return true;
            }

            return false;
        }

        // Checks if the word ends with any of the special characters defined in Lookups.SpecialCharacters.
        private static bool WordEndsWithSpecialCharacter(string word)
        {
            for (int i = 0; i < Lookups.SpecialCharacters.Length; i++)
            {
                if (word.StartsWith(Lookups.SpecialCharacters[i].ToString().ToLower()))
                    return true;
            }

            return false;
        }

        // Removes any special characters from the start or end of the word as defined in Lookups.SpecialCharacters.
        private static string RemoveStartOrEndsWithSpecialCharacter(string word, out char? start, out char? end)
        {
            start = null;
            end = null;

            if (WordStartsWithSpecialCharacter(word))
            {
                start = word[0];
                word = word.Substring(1);
            }

            if (WordEndsWithSpecialCharacter(word))
            {
                end = word[word.Length - 1];
                word = word.Substring(0, word.Length - 1);
            }

            return word;
        }

        // Removes any words from the Lookups.LowercaseHashSet that are present in the additionalAbbreviations list.
        private static void RemoveFromLowercase(List<string> additionalAbbreviations)
        {
            if (additionalAbbreviations != null && additionalAbbreviations.Count > 0)
            {
                foreach (var abbreviation in additionalAbbreviations)
                {
                    if (Lookups.LowercaseHashSet.Contains(abbreviation.ToLower()))
                        Lookups.LowercaseHashSet.Remove(abbreviation.ToLower());
                }
            }
        }

        // Removes any words from the Lookups.AbbreviatedDayOrMonthHashSet that are present in the additionalAbbreviations list.
        private static void RemoveFromAbbreviatedDayOrMonth(List<string> additionalAbbreviations)
        {
            if (additionalAbbreviations != null && additionalAbbreviations.Count > 0)
            {
                foreach (var abbreviation in additionalAbbreviations)
                {
                    if (Lookups.AbbreviatedDayOrMonthHashSet.Contains(abbreviation.ToLower()))
                        Lookups.AbbreviatedDayOrMonthHashSet.Remove(abbreviation.ToLower());
                }
            }
        }

        // Removes any words from the Lookups.SmallWordsHash that are present in the additionalAbbreviations list.
        private static void RemoveFromSmallWords(List<string> additionalAbbreviations)
        {
            if (additionalAbbreviations != null && additionalAbbreviations.Count > 0)
            {
                foreach (var abbreviation in additionalAbbreviations)
                {
                    if (Lookups.SmallWordsHash.Contains(abbreviation.ToLower()))
                        Lookups.SmallWordsHash.Remove(abbreviation.ToLower());
                }
            }
        }

