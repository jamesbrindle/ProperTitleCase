        /// <summary>
        /// Converts the input string to title case, with various options for handling abbreviations, measurements, and other special cases.
        /// </summary>
        /// <param name="input">The string to convert to title case.</param>
        /// <param name="additionalAbbreviations">A list of additional abbreviations to consider du^ring conversion.</param>
        /// <param name="lookupCommonAbbreviations">Whether to look up common abbreviations.</param>
        /// <param name="keepTypicalAllLowers">Whether to keep typically lowercase words in lower case.</param>
        /// <param name="formatMeasurements">Whether to format measurement strings.</param>
        /// <param name="removeStartEndQuotesOnClean">Remove preceeding and leeding apostrophe or double quote (if end quote is same at start quote and if end quote is not a measurement, i.e.19")</param>
        /// <param name="removeDoubleSymbols">Remove doulbe symbols ( "", '', --) on clean</param>
        /// <returns>A string converted to title case.</returns>
        public static string ToProperTitleCase(
            this string input,
            List<string> additionalAbbreviations = null,
            bool lookupCommonAbbreviations = true,
            bool keepTypicalAllLowers = true,
            bool formatMeasurements = true,
            bool removeStartEndQuotesOnClean = true,
            bool removeDoubleSymbols = true)
        {
            // Converts a single string to title case using the specified options.
            return ToProperTitleCase(
                new List<string> { input },
                additionalAbbreviations,
                lookupCommonAbbreviations,
                keepTypicalAllLowers,
                formatMeasurements,
                removeStartEndQuotesOnClean,
                removeDoubleSymbols).FirstOrDefault();
        }

        /// <summary>
        /// Converts an array of strings to title case.
        /// </summary>
        /// <param name="input">The array of strings to convert.</param>
        /// <param name="additionalAbbreviations">A list of additional abbreviations to consider during conversion.</param>
        /// <param name="lookupCommonAbbreviations">Whether to look up common abbreviations.</param>
        /// <param name="keepTypicalAllLowers">Whether to keep typically lowercase words in lower case.</param>
        /// <param name="formatMeasurements">Whether to format measurement strings.</param>
        /// <param name="removeStartEndQuotesOnClean">Remove preceeding and leeding apostrophe or double quote (if end quote is same at start quote and if end quote is not a measurement, i.e.19")</param>
